#!/user/bin/bash

clear
echo ' ############################### Namespaces #################################### '
kubectl get namespaces -A
echo ' ############################################################################### '
echo ' Enter the namespace to tshoot : '
read ns
clear
echo ' ############################ Namespace Events ################################# '
kubectl get events -n $ns  | grep "error\|Error\|warning\|Warning"
echo ' ############################################################################### '
echo ' ------------------------------------------------------------------------------- '
echo ' Enter to Continue ... '
read c
echo ' ------------------------------------------------------------------------------- '

clear
echo ' ############################ All In Namespace ################################# '
kubectl get pods -n $ns
echo ' ############################################################################### '
echo ' ------------------------------------------------------------------------------- '
echo ' Enter the pod name to describe : '
read pod
echo ' ------------------------------------------------------------------------------- '
clear
echo ' ################################ Pod Describe  ################################ '
kubectl get pod -n $ns $pod -o yaml | grep "error\|Error"
echo ' ############################################################################### '
echo ' ------------------------------------------------------------------------------- '
echo ' Enter to Continue ... '
read c
echo ' ------------------------------------------------------------------------------- '

clear
echo ' ------------------------------------------------------------------------------- '
echo ' ############################### Namespaces #################################### '
kubectl get pods -n $ns
echo ' ############################################################################### '
echo ' ------------------------------------------------------------------------------- '
echo ' Enter the pod name to show logs: '
read pod
echo ' ------------------------------------------------------------------------------- '
clear
echo ' ################################ POD LOGS ##################################### '
kubectl logs -n $ns $pod | grep "error\|Error"
echo ' ############################################################################### '
echo ' ------------------------------------------------------------------------------- '
